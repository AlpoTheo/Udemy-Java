public class Veritipleri {
    public static void main(String[] args) {
        double a = 4d;
        System.out.println(a);
        float b = (float) 5.0;
        System.out.println(b);
        // Double de daha doğru ondalıklı sayı verileri elde ederiz. int --> float ---> double Otomatik dönüştürme
    
        char a1 = 'A';
        char b1 = 1000;
        char d = '\u0152'; // unicode table
        System.out.println(d);

        boolean a3 = true;
        System.out.println(a3);

        String a5 = "Merhaba";
        System.out.println(a5 + " Hoşgeldiniz");

        String b5 = "Java\tProgramlama\tDili";
        System.out.println(b5);

        String b6 = "Java\nProgramlama\nDili";
        System.out.println(b6);


        int aa = 7;
        System.out.println(++aa);  // postfix prefix fark prefix de güncellenmiş değer yazar postfix de yazmaz.
        System.out.println(aa++);





    
    
    }    
}
